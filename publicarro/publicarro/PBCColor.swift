import UIKit
import Foundation

extension UIColor{
    
    class func PBCBackGorundColor() -> UIColor{
        return UIColor(red:0.21, green:0.23, blue:0.27, alpha:1.0)
    }

    class func PBCButtonBackGroundColor() -> UIColor{
        return UIColor(red:0.20, green:0.20, blue:0.20, alpha:1.0)
    }
    
    class func blue() -> UIColor{
        return UIColor.blueColor()
    }
    
    class func green() -> UIColor{
        return UIColor.greenColor()
    }
    
    
}